#include "ft_printf.h"

int	ft_putstr_fd(char *s, int i)
{
	int	a;

	if (s == NULL)
		return (0);
	a = 0;
	while (s[a] != '\0' && i > 0)
	{
		write(1, &s[a], 1);
		a++;
		i--;
	}
	return (a);
}
